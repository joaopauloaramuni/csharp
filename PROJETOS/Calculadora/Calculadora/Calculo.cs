using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Calculadora
{
    class Calculo
    {
        public double soma(double x, double y)
        {
           return x + y;
        }
        public double sub(double x, double y)
        {
            return x - y;
        }
        public double mult(double x, double y)
        {
            return x * y;
        }
        public double div(double x, double y)
        {
            return x / y;
        }
    }
}
